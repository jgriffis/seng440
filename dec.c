void huffman_decode(unsigned char *input, int len, char *output) {
    int i;
    int in_idx, out_idx;
    unsigned char val, lookup, run;

    i = 0;    
    in_idx = 0;
    out_idx = 0;
    val = 0;
    
    while (in_idx < len) {
        for (; i < MAX_CODE_LEN; i++) {
            if (in_idx + i == len) {
                val <<= (MAX_CODE_LEN - i);
                break;
            }
            
            val <<= 1;
            val |= input[in_idx + i];
        }
        
        lookup = HD_LUT[val & 0x7F];
        output[out_idx++] = (lookup >> 4) + ASCII_SHIFT;
        
        run = lookup & 0x0F;
        in_idx += run;
        i = MAX_CODE_LEN - run;
    }
    
    output[out_idx] = '\0';
}

int main(void) {
    char input[512];
    char output[512];
    int i;
    
    scanf("%s", input);
    // Convert '0' and '1' to integers 0 and 1, respectively
    for (i = 0; input[i] != '\0'; i++) {
        input[i] = input[i] - 0x30;
    }

    huffman_decode(input, i, output);
    printf("%s\n", output);
}

