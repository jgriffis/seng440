#define HUFFMAN_DECODE(input, len, output) \
    int i; \
    int in_idx, out_idx; \
    unsigned char val, lookup, run; \
    \
    i = 0; \
    in_idx = 0; \
    out_idx = 0; \
    \
    while (in_idx < len) { \
        for (; i < MAX_CODE_LEN; i++) { \
            if (in_idx + i >= len) { \
                val <<= (MAX_CODE_LEN - i); \
                break; \
            } \
            \
            val <<= 1; \
            val |= input[in_idx + i]; \
        } \
        \
        lookup = HD_LUT[val & 0x7F]; \
        output[out_idx++] = (lookup >> 4) + ASCII_SHIFT; \
        \
        run = lookup & 0x0F; \
        in_idx += run; \
        i = MAX_CODE_LEN - run; \
    } \
    \
    output[out_idx] = '\0';

int main(void) {
    char buffer[512];
    unsigned char input[512];
    int j;
    
    scanf("%s", buffer);
    for (j = 0; buffer[j] != '\0'; j++) {
        input[j] = buffer[j] - 0x30;
    }

    HUFFMAN_DECODE(input, j, buffer);
    printf("%s\n", buffer);
}

